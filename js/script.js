let currentLang = localStorage.getItem('bgenius_lang') || 'en';

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('bgenius_lang', lang);
  const html = document.getElementById('html-root');
  html.setAttribute('lang', lang);
  html.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');

  // Update lang buttons
  ['en', 'ar'].forEach(l => {
    const main = document.getElementById(`btn-${l}`);
    const mob = document.getElementById(`m-btn-${l}`);
    if (main) {
      main.classList.remove('active', 'inactive');
      main.classList.add(l === lang ? 'active' : 'inactive');
    }
    if (mob) {
      mob.classList.remove('active', 'inactive');
      mob.classList.add(l === lang ? 'active' : 'inactive');
    }
  });

  // Apply translations
  const t = i18n[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.textContent = t[key];
  });
  document.querySelectorAll('[data-i18n-m]').forEach(el => {
    const key = el.getAttribute('data-i18n-m');
    if (t[key] !== undefined) el.textContent = t[key];
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (t[key] !== undefined) el.setAttribute('placeholder', t[key]);
  });

  // Re-render dynamic components
  renderPrograms();
  renderPricing();
  renderTestimonials();
  renderDaysCheckboxes();
  renderFooterPrograms();
  updateStepUI();
}

