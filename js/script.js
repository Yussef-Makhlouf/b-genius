let currentStep = 1;
const totalSteps = 1;

const stepMeta = {
  1: { titleKey: 'step1_title', subKey: 'step1_sub' },
};

function renderProgressBar() {
  const bar = document.getElementById('progress-bar');
  if (!bar) return;
  let html = '';
  for (let i = 1; i <= totalSteps; i++) {
    const done = i < currentStep;
    const active = i === currentStep;
    const cls = done ? 'done' : active ? 'active' : '';
    html += `<div class="progress-step ${cls}">${done ? '✓' : i}</div>`;
    if (i < totalSteps) {
      html += `<div class="progress-line ${done ? 'done' : ''}"></div>`;
    }
  }
  bar.innerHTML = html;
}

function updateStepUI() {
  renderProgressBar();
  const t = i18n[currentLang];
  const meta = stepMeta[currentStep];
  if (meta) {
    const title = document.getElementById('step-title');
    const sub = document.getElementById('step-subtitle');
    if (title) title.textContent = t[meta.titleKey] || '';
    if (sub) sub.textContent = t[meta.subKey] || '';
  }

  // Show/hide panels
  for (let i = 1; i <= totalSteps; i++) {
    const panel = document.getElementById(`step-${i}`);
    if (panel) panel.classList.toggle('active', i === currentStep);
  }

  // Prev/Next buttons
  const prev = document.getElementById('btn-prev');
  const next = document.getElementById('btn-next');
  const nav = document.getElementById('form-nav');

  if (currentStep === 1) {
    prev.classList.add('hidden');
  } else {
    prev.classList.remove('hidden');
  }

  if (currentStep === totalSteps) {
    next.style.display = 'none';
    nav.style.display = 'none';
  } else {
    next.style.display = 'inline-flex';
    nav.style.display = 'flex';
  }

  // Step 4: build review
  if (currentStep === 4) buildReview();
}

function nextStep() {
  if (currentStep < totalSteps) {
    currentStep++;
    updateStepUI();
  }
}

function prevStep() {
  if (currentStep > 1) {
    currentStep--;
    updateStepUI();
  }
}

